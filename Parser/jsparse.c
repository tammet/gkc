// like: ((u:include u:Axioms/SWC001-0.ax))

int wr_is_json_import_clause(void* db, void* cl) {
  void* fun;

  if (wg_isatom(db,cl)) return 0;
  fun=wg_first(db,cl);
  if (!wg_islog_uri(db,fun,"include")) return 0;
  if (wg_list_len(db,cl)!=2) return 0;
  return 1;
}


